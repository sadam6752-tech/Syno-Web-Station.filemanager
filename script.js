async function downloadSelectedFiles() {
    if (selectedFiles.size === 0) return;
    
    const files = Array.from(selectedFiles);
    
    if (files.length === 1) {
        downloadFile(files[0]);
    } else {
        // Download as ZIP
        try {
            const formData = new FormData();
            formData.append('action', 'download_multiple');
            formData.append('files', JSON.stringify(files));
            
            const response = await fetch('api.php', {
                method: 'POST',
                body: formData
            });
            
            if (response.ok) {
                const blob = await response.blob();
                const url = window.URL.createObjectURL(blob);
                const a = document.createElement('a');
                a.href = url;
                a.download = 'files.zip';
                document.body.appendChild(a);
                a.click();
                window.URL.revokeObjectURL(url);
                document.body.removeChild(a);
            } else {
                throw new Error('Download failed');
            }
        } catch (error) {
            alert('Error: ' + error.message);
        }
    }
}

async function deleteSelectedFiles() {
    if (selectedFiles.size === 0) return;
    
    const count = selectedFiles.size;
    const message = count === 1 ? t('confirmDelete') : t('confirmDeleteMultiple').replace('{count}', count);
    
    if (!confirm(message)) return;
    
    const files = Array.from(selectedFiles);
    
    try {
        const formData = new FormData();
        formData.append('action', 'delete_multiple');
        formData.append('files', JSON.stringify(files));
        
        const response = await fetch('api.php', {
            method: 'POST',
            body: formData
        });
        
        const text = await response.text();
        let data;
        
        try {
            data = JSON.parse(text);
        } catch (e) {
            console.error('Invalid JSON response:', text);
            throw new Error('Server returned invalid response');
        }
        
        if (!data.success) throw new Error(data.error);
        
        selectedFiles.clear();
        loadFiles();
    } catch (error) {
        alert('Error: ' + error.message);
    }
}

async function moveSelectedUp() {
    if (selectedFiles.size === 0 || currentPath === '') return;
    
    const files = Array.from(selectedFiles);
    const parentPath = currentPath.substring(0, currentPath.lastIndexOf('/'));
    
    let successCount = 0;
    let errorCount = 0;
    
    for (const file of files) {
        try {
            await moveItem(file, parentPath);
            successCount++;
        } catch (error) {
            errorCount++;
            console.error('Error moving file:', file, error);
        }
    }
    
    selectedFiles.clear();
    loadFiles();
    
    if (errorCount > 0) {
        alert(`Moved ${successCount} items. ${errorCount} errors occurred.`);
    }
}

