function formatSize(bytes) {
    if (bytes === 0) return '0 B';
    const k = 1024;
    const sizes = ['B', 'KB', 'MB', 'GB'];
    const i = Math.floor(Math.log(bytes) / Math.log(k));
    return Math.round(bytes / Math.pow(k, i) * 100) / 100 + ' ' + sizes[i];
}

function formatDate(timestamp) {
    return new Date(timestamp * 1000).toLocaleString(currentLang);
}

function updateBreadcrumb() {
    const breadcrumb = document.getElementById('breadcrumb');
    const parts = currentPath ? currentPath.split('/') : [];
    
    let html = `<span class="breadcrumb-item" data-path="" onclick="navigateTo('')"><i class="fas fa-home"></i> ${t('home')}</span>`;
    
    let path = '';
    parts.forEach((part, index) => {
        path += (index > 0 ? '/' : '') + part;
        html += ` / <span class="breadcrumb-item" data-path="${path}" onclick="navigateTo('${path}')">${part}</span>`;
    });
    
    breadcrumb.innerHTML = html;
    
    // Добавляем drop zone для breadcrumb элементов
    document.querySelectorAll('.breadcrumb-item').forEach(item => {
        const targetPath = item.dataset.path;
        
        item.addEventListener('dragover', (e) => {
            e.preventDefault();
            e.stopPropagation();
            if (draggedPath && draggedPath !== targetPath && !draggedPath.startsWith(targetPath + '/')) {
                item.classList.add('drag-over');
            }
        });
        
        item.addEventListener('dragleave', () => {
            item.classList.remove('drag-over');
        });
        
        item.addEventListener('drop', async (e) => {
            e.preventDefault();
            e.stopPropagation();
            item.classList.remove('drag-over');
            
            if (draggedPath && draggedPath !== targetPath) {
                // Проверяем, что не перетаскиваем в ту же папку
                const draggedDir = draggedPath.substring(0, draggedPath.lastIndexOf('/'));
                if (draggedDir !== targetPath) {
                    await moveItem(draggedPath, targetPath);
                }
            }
        });
    });
}

async function loadFiles() {
    try {
        const response = await fetch(`api.php?action=list&path=${encodeURIComponent(currentPath)}`);
        const data = await response.json();
        
        if (!data.success) throw new Error(data.error);
        
        allFiles = data.items; // Сохраняем для поиска
        
        const fileList = document.getElementById('file-list');
        fileList.innerHTML = '';
        
        // Сохраняем выбранные файлы в режиме перемещения
        const savedSelection = moveMode ? new Set(selectedFiles) : null;
        
        if (!moveMode) {
            selectedFiles.clear();
        }
        
        data.items.forEach(item => {
            fileList.appendChild(createFileElement(item));
        });
        
        // Восстанавливаем выбранные файлы в режиме перемещения
        if (moveMode && savedSelection) {
            selectedFiles = savedSelection;
        }
        
        updateSelectionUI();
        updateBreadcrumb();
        loadFolderStats();
        
        // Применить текущий поиск если есть
        const searchInput = document.getElementById('search-input');
        if (searchInput.value) {
            performSearch(searchInput.value);
        }
    } catch (error) {
        alert('Error: ' + error.message);
    }
}

async function loadFolderStats() {
    const folderStatsEl = document.getElementById('folder-stats');
    
    if (!folderStatsEl) return;
    
    folderStatsEl.innerHTML = `<i class="fas fa-spinner fa-spin"></i> ${t('calculating')}`;
    
    try {
        const response = await fetch(`api.php?action=folder_stats&path=${encodeURIComponent(currentPath)}`);
        const data = await response.json();
        
        if (!data.success) throw new Error(data.error);
        
        const stats = data.stats;
        folderStatsEl.innerHTML = `
            <i class="fas fa-chart-bar"></i>
            ${stats.files} ${t('files')}, ${stats.folders} ${t('folders')} (${formatSize(stats.size)})
        `;
    } catch (error) {
        folderStatsEl.innerHTML = '';
        console.error('Error loading folder stats:', error);
    }
}

function updateSelectionStats() {
    const selectionStatsEl = document.getElementById('selection-stats');
    
    if (!selectionStatsEl) return;
    
    if (selectedFiles.size === 0) {
        selectionStatsEl.style.display = 'none';
        return;
    }
    
    // Подсчитываем общий размер выбранных файлов
    let totalSize = 0;
    let fileCount = 0;
    let folderCount = 0;
    
    selectedFiles.forEach(path => {
        const item = allFiles.find(f => f.path === path);
        if (item) {
            if (item.type === 'file') {
                totalSize += item.size;
                fileCount++;
            } else {
                folderCount++;
            }
        }
    });
    
    let statsText = `<i class="fas fa-check-circle"></i> ${selectedFiles.size} ${t('selected')}`;
    
    if (fileCount > 0) {
        statsText += ` (${formatSize(totalSize)})`;
    }
    
    selectionStatsEl.innerHTML = statsText;
    selectionStatsEl.style.display = 'flex';
}

function performSearch(query) {
    const fileItems = document.querySelectorAll('.file-item');
    const searchQuery = query.toLowerCase().trim();
    
    if (!searchQuery) {
        // Показать все файлы
        fileItems.forEach(item => {
            item.classList.remove('search-hidden', 'search-highlight');
        });
        document.getElementById('search-clear').style.display = 'none';
        return;
    }
    
    document.getElementById('search-clear').style.display = 'block';
    
    let visibleCount = 0;
    
    fileItems.forEach(item => {
        const fileName = item.querySelector('.file-name').textContent.toLowerCase();
        
        if (fileName.includes(searchQuery)) {
            item.classList.remove('search-hidden');
            item.classList.add('search-highlight');
            visibleCount++;
        } else {
            item.classList.add('search-hidden');
            item.classList.remove('search-highlight');
        }
    });
    
    // Можно добавить счетчик найденных файлов
    console.log(`Found ${visibleCount} files matching "${query}"`);
}

function clearSearch() {
    const searchInput = document.getElementById('search-input');
    searchInput.value = '';
    performSearch('');
    searchInput.focus();
}

function createFileElement(item) {
    const div = document.createElement('div');
    div.className = 'file-item';
    div.draggable = true;
    div.dataset.path = item.path;
    
    const checkbox = `<input type="checkbox" class="file-checkbox" data-path="${item.path}" onclick="toggleFileSelection(event, '${item.path}')">`;
    
    // Определяем, показывать ли миниатюру
    const showThumbnail = item.thumbnail && item.isImage;
    const iconOrThumb = showThumbnail 
        ? `<img src="${item.thumbnail}" class="file-thumbnail" alt="${item.name}">`
        : `<i class="fas ${item.icon}"></i>`;
    
    if (currentView === 'grid') {
        div.innerHTML = `
            ${checkbox}
            ${iconOrThumb}
            <div class="file-name">${item.name}</div>
        `;
    } else {
        div.innerHTML = `
            ${checkbox}
            <div class="file-info">
                ${showThumbnail ? `<img src="${item.thumbnail}" class="file-thumbnail-small" alt="${item.name}">` : `<i class="fas ${item.icon}"></i>`}
                <div class="file-name">${item.name}</div>
            </div>
            <div class="file-details">
                <span>${item.type === 'file' ? formatSize(item.size) : '-'}</span>
                <span>${formatDate(item.modified)}</span>
            </div>
            <div class="file-actions">
                <button onclick="showFileInfo('${item.path}')" title="${t('details')}">
                    <i class="fas fa-info-circle"></i>
                </button>
                <button onclick="renameItem('${item.path}')" title="${t('rename')}">
                    <i class="fas fa-edit"></i>
                </button>
                <button onclick="deleteItem('${item.path}')" title="${t('delete')}">
                    <i class="fas fa-trash"></i>
                </button>
            </div>
        `;
    }
    
    if (item.type === 'folder') {
        div.onclick = (e) => {
            // Игнорируем клик на чекбокс и кнопки действий
            if (e.target.classList.contains('file-checkbox') || 
                e.target.closest('.file-actions') ||
                (e.target.tagName === 'BUTTON')) {
                return;
            }
            
            // Если режим перемещения активен
            if (moveMode) {
                e.preventDefault();
                e.stopPropagation();
                moveSelectedToFolder(item.path);
                return;
            }
            
            navigateTo(item.path);
        };
        
        // Добавляем класс для режима перемещения
        if (moveMode) {
            div.classList.add('move-target');
            div.style.cursor = 'pointer';
        }
        
        setupDropTarget(div, item.path);
    } else {
        div.onclick = (e) => {
            // Игнорируем клик на чекбокс и кнопки действий
            if (e.target.classList.contains('file-checkbox') || 
                e.target.closest('.file-actions') ||
                (e.target.tagName === 'BUTTON')) {
                return;
            }
            downloadFile(item.path);
        };
    }
    
    setupDragSource(div, item.path);
    
    return div;
}

function setupDragSource(element, path) {
    element.addEventListener('dragstart', (e) => {
        draggedPath = path;
        element.classList.add('dragging');
        e.dataTransfer.effectAllowed = 'move';
    });
    
    element.addEventListener('dragend', () => {
        element.classList.remove('dragging');
        draggedPath = null;
    });
}

function setupDropTarget(element, targetPath) {
    element.addEventListener('dragover', (e) => {
        e.preventDefault();
        e.stopPropagation();
        if (draggedPath && draggedPath !== targetPath) {
            element.classList.add('drag-over');
        }
    });
    
    element.addEventListener('dragleave', () => {
        element.classList.remove('drag-over');
    });
    
    element.addEventListener('drop', async (e) => {
        e.preventDefault();
        e.stopPropagation();
        element.classList.remove('drag-over');
        
        if (draggedPath && draggedPath !== targetPath) {
            await moveItem(draggedPath, targetPath);
        }
    });
}

async function moveItem(sourcePath, targetDir) {
    try {
        const formData = new FormData();
        formData.append('action', 'move');
        formData.append('sourcePath', sourcePath);
        formData.append('targetDir', targetDir);
        
        const response = await fetch('api.php', {
            method: 'POST',
            body: formData
        });
        
        const text = await response.text();
        let data;
        
        try {
            data = JSON.parse(text);
        } catch (e) {
            console.error('Invalid JSON response:', text);
            throw new Error('Server returned invalid response');
        }
        
        if (!data.success) throw new Error(data.error);
        
        loadFiles();
    } catch (error) {
        alert('Error: ' + error.message);
    }
}

