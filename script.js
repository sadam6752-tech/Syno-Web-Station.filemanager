async function uploadFiles(files) {
    if (files.length === 0) return;
    
    const progressContainer = document.getElementById('upload-progress');
    const progressFill = document.getElementById('upload-progress-fill');
    const progressText = document.getElementById('upload-progress-text');
    const progressDetails = document.getElementById('upload-progress-details');
    
    progressContainer.classList.add('active');
    
    let completed = 0;
    const total = files.length;
    
    for (const file of files) {
        try {
            progressText.textContent = `Uploading ${file.name}...`;
            progressDetails.textContent = `${completed + 1} of ${total} files`;
            
            const formData = new FormData();
            formData.append('action', 'upload');
            formData.append('path', currentPath);
            formData.append('file', file);
            
            const xhr = new XMLHttpRequest();
            
            // Track upload progress
            xhr.upload.addEventListener('progress', (e) => {
                if (e.lengthComputable) {
                    const fileProgress = (e.loaded / e.total) * 100;
                    const totalProgress = ((completed + (fileProgress / 100)) / total) * 100;
                    progressFill.style.width = totalProgress + '%';
                }
            });
            
            // Wait for upload to complete
            await new Promise((resolve, reject) => {
                xhr.onload = () => {
                    console.log('Upload response status:', xhr.status);
                    console.log('Upload response text:', xhr.responseText);
                    
                    if (xhr.status === 200) {
                        try {
                            const data = JSON.parse(xhr.responseText);
                            if (data.success) {
                                resolve();
                            } else {
                                reject(new Error(data.error || 'Upload failed'));
                            }
                        } catch (e) {
                            console.error('Parse error:', e);
                            console.error('Response:', xhr.responseText);
                            reject(new Error('Invalid server response: ' + xhr.responseText.substring(0, 100)));
                        }
                    } else if (xhr.status === 400) {
                        // Try to parse error from response
                        try {
                            const data = JSON.parse(xhr.responseText);
                            reject(new Error(data.error || 'Bad request'));
                        } catch (e) {
                            reject(new Error(`HTTP 400: ${xhr.responseText.substring(0, 100)}`));
                        }
                    } else {
                        reject(new Error(`HTTP ${xhr.status}: ${xhr.statusText}`));
                    }
                };
                
                xhr.onerror = () => reject(new Error('Network error'));
                xhr.ontimeout = () => reject(new Error('Upload timeout'));
                
                xhr.open('POST', 'api.php', true);
                xhr.timeout = 300000; // 5 minutes timeout
                xhr.send(formData);
            });
            
            completed++;
            const totalProgress = (completed / total) * 100;
            progressFill.style.width = totalProgress + '%';
            
        } catch (error) {
            console.error('Error uploading ' + file.name + ':', error);
            progressText.textContent = `Error: ${file.name}`;
            progressDetails.textContent = error.message;
            
            // Keep error visible longer
            await new Promise(resolve => setTimeout(resolve, 3000));
        }
    }
    
    if (completed === total) {
        progressText.textContent = `Upload complete!`;
        progressDetails.textContent = `${completed} of ${total} files uploaded`;
    } else {
        progressText.textContent = `Upload finished with errors`;
        progressDetails.textContent = `${completed} of ${total} files uploaded successfully`;
    }
    
    // Hide progress after 3 seconds
    setTimeout(() => {
        progressContainer.classList.remove('active');
        progressFill.style.width = '0%';
    }, 3000);
    
    loadFiles();
}

